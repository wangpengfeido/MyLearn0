///////////////////////////////////////////////
//Object.assign实行的是浅拷贝
console.log(Object.assign(
    {a: {b: 10, c: 100}},
    {a: {d: 1000}})
);

//////////////////////////////////////////
// 同名属性会被替换
console.log(Object.assign({a: 12}, {a: 34}));

