// Array.from()可以将
// 类似数组的对象(具有length属性)
// 和可遍历（iterable）对象
// 转换为数组
///////////////////////////////////////////////////////
let arrayLike = {0: 'a', 1: 'b', length: 2};
console.log(Array.from(arrayLike));
// 实际应用是arguments对象和NodeList集合

////////////////////////////////////////////////////////
let s = new Set([1, 2, 3]);
console.log(Array.from(s));

///////////////////////////////////////////////////////
// 它与扩展运算符转换数组不同的是，扩展运算符的转换不能转换类似数组的对象

