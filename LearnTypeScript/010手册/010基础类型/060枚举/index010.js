// 枚举可以为一组数值赋予友好的名字
var A;
(function (A) {
    A[A["A"] = 0] = "A";
    A[A["B"] = 1] = "B";
    A[A["C"] = 2] = "C";
})(A || (A = {}));
// 可以指定开始编号
var B;
(function (B) {
    B[B["A"] = 1] = "A";
    B[B["B"] = 2] = "B";
    B[B["C"] = 3] = "C";
})(B || (B = {}));
// 也可以全部手动赋值
var C;
(function (C) {
    C[C["A"] = 2] = "A";
    C[C["B"] = 4] = "B";
    C[C["C"] = 8] = "C";
})(C || (C = {}));
// 枚举值和枚举名可以互相查找
console.log(C['A'], C[4]);
